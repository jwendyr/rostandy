export type Locale = 'en' | 'id' | 'ja' | 'ko' | 'zh' | 'fr' | 'de' | 'es' | 'pt' | 'ar' | 'hi' | 'ru' | 'tr' | 'it' | 'nl';

const supportedLocales: Locale[] = ['en', 'id', 'ja', 'ko', 'zh', 'fr', 'de', 'es', 'pt', 'ar', 'hi', 'ru', 'tr', 'it', 'nl'];

export function detectLocaleFromHeader(acceptLang: string | null): Locale {
  if (!acceptLang) return 'en';
  const langs = acceptLang
    .split(',')
    .map(part => {
      const [tag, q] = part.trim().split(';q=');
      return { code: tag.split('-')[0].toLowerCase() as Locale, q: q ? parseFloat(q) : 1.0 };
    })
    .sort((a, b) => b.q - a.q);
  for (const { code } of langs) {
    if (supportedLocales.includes(code)) return code;
  }
  return 'en';
}

export function isRTL(locale: Locale): boolean {
  return locale === 'ar';
}

export function getLocaleFromNavigator(): Locale {
  if (typeof navigator === 'undefined') return 'en';
  const code = navigator.language?.split('-')[0]?.toLowerCase() as Locale;
  return supportedLocales.includes(code) ? code : 'en';
}

